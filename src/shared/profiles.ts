export function pickRandomProfile(): HardwareProfile {
  const base = PROFILES[Math.floor(Math.random() * PROFILES.length)]!;
  return {
    ...base,
    id: Date.now().toString(36) + Math.random().toString(36).slice(2, 8),
    version: 2,
    createdAt: Date.now(),
    clockSkew: (Math.random() - 0.5) * 2000,
    battery: {
      ...base.battery,
      level: clampBattery(base.battery.level + (Math.random() - 0.5) * 0.1),
    },
  };
}

export function deriveProfile(
  profile: HardwareProfile,
  origin: string,
): HardwareProfile {
  const baseId = profile.id || "base";
  const seed = hashOrigin(baseId, origin);
  const noise = new SeededRNG(seed);

  const derivedId = baseId + ":" + seed.toString(36);

  return {
    ...profile,
    id: derivedId,
    cores: noise.int(profile.cores - 1, profile.cores + 1),
    screenW: noise.int(profile.screenW - 4, profile.screenW + 4),
    screenH: noise.int(profile.screenH - 4, profile.screenH + 4),
    lat: profile.lat + noise.float(-0.005, 0.005),
    lon: profile.lon + noise.float(-0.005, 0.005),
    downlink: noise.int(profile.downlink - 5, profile.downlink + 5),
    rtt: noise.int(profile.rtt - 5, profile.rtt + 5),
    battery: {
      ...profile.battery,
      level: clampBattery(profile.battery.level + noise.float(-0.03, 0.03)),
    },
  };
}

class SeededRNG {
  private state: number;
  constructor(seed: number) {
    this.state = seed;
  }
  next(): number {
    this.state ^= this.state << 13;
    this.state ^= this.state >>> 17;
    this.state ^= this.state << 5;
    return (this.state >>> 0) / 0xffffffff;
  }
  int(min: number, max: number): number {
    return Math.floor(min + this.next() * (max - min + 1));
  }
  float(min: number, max: number): number {
    return min + this.next() * (max - min);
  }
}

function hashOrigin(id: string, origin: string): number {
  const str = `${id}:${origin}`;
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = (hash << 5) - hash + str.charCodeAt(i);
    hash |= 0;
  }
  return hash >>> 0;
}

function clampBattery(val: number): number {
  return Math.max(0.05, Math.min(1, val));
}

