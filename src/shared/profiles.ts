import type { HardwareProfile } from "./types";

export const PROFILE_VERSION = 2;

function migrateProfile(profile: Partial<HardwareProfile>): HardwareProfile {
  const base = p({});
  return {
    ...base,
    ...profile,
    version: PROFILE_VERSION,
    createdAt: profile.createdAt || Date.now(),
    fullGpu: profile.fullGpu || profile.gpuRenderer || base.fullGpu,
    userAgent: profile.userAgent || base.userAgent,
    plugins: profile.plugins || [],
    mimeTypes: profile.mimeTypes || [],
    country: profile.country || inferCountry(profile.timezone || base.timezone),
    isMobile: profile.isMobile ?? (profile.touchPts ?? 0) > 0,
    availW: profile.availW ?? profile.screenW ?? base.availW,
    availH: profile.availH ?? (profile.screenH ?? base.screenH) - 40,
  };
}

function inferCountry(timezone: string): string {
  if (timezone.startsWith("America/")) return "US";
  if (timezone.startsWith("Europe/")) return "GB";
  if (timezone.startsWith("Asia/")) return "JP";
  if (timezone.startsWith("Australia/")) return "AU";
  if (timezone.startsWith("Africa/")) return "ZA";
  return "US";
}

